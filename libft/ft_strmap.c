#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	int		i;
	char	*str;

	str = ft_memalloc(ft_strlen(s) + 1);
	i = 0;
	if (s && str)
	{
		if (!str)
			return (NULL);
		while (s[i])
		{
			str[i] = (*f)(s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
