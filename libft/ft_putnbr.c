#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int x;

	x = n;
	if (n < 0)
	{
		ft_putchar('-');
		x = (unsigned int)n * (-1);
	}
	if (x >= 10)
	{
		ft_putnbr(x / 10);
		ft_putnbr(x % 10);
	}
	else
		ft_putchar(x + '0');
}
