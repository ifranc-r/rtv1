#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	a;
	char	*tmp;

	a = 0;
	tmp = s1;
	i = ft_strlen(s1);
	while (s2[a] != '\0' && n != 0)
	{
		s1[i++] = s2[a++];
		n--;
	}
	s1[i] = '\0';
	return (s1);
}
